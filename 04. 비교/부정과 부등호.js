/*
! 사용할 경우 부정을 의미
>, >=, <, <= 크기 비고시 사용
*/

console.log(1 != 2); // true
console.log(1 != 1); // false
console.log("one" != "two"); // true
console.log("one" != "one"); // false

console.log(10 > 20); // false
console.log(10 > 1); // true
console.log(10 > 10); // false

console.log( 10 >= 20); //false
console.log(10 >= 1); // true
console.log(10 >= 10); // true